int main(void)
{
  u8* pf_texture_a = NULL;
  u8* pf_texture_b = NULL;
  u16* pf_texture_lut = NULL;

  /* Initialize Ultra64 */
  console_init();
  controller_init();
  console_set_render_mode(RENDER_MANUAL);
  display_init(RESOLUTION_320x240, DEPTH_32_BPP, 3, GAMMA_NONE, FILTERS_RESAMPLE);
  rdpq_init();

  audio_init(PF_SOUND_FREQUENCY, 4);

  /* Initialize emulator */
  pressf_init(&f8_system);
  f8_system_init(&f8_system, &pf_systems[0]);
  f8_write(&f8_system, 0x0000, bios_a, 0x0400);
  f8_write(&f8_system, 0x0400, bios_b, 0x0400);

  pf_texture_a = (u8*)malloc_uncached_aligned(64, 64 * 64 / 2);
  pf_texture_b = (u8*)malloc_uncached_aligned(64, 64 * 64 / 2);
  pf_texture_lut = (u16*)malloc(16 * 2);
  memcpy(pf_texture_lut, pf_texture_lut_data, sizeof(pf_texture_lut_data));
  rdpq_tex_upload_tlut(pf_texture_lut, 0, 16);

  /* Main loop test */
  while (1)
  { 
    struct controller_data keys;

    console_clear();

    controller_scan();
    controller_read(&keys);

    /* Handle console input */
    set_input_button(0, INPUT_TIME, keys.c[0].A);
    set_input_button(0, INPUT_MODE, keys.c[0].B);
    set_input_button(0, INPUT_HOLD, keys.c[0].Z);
    set_input_button(0, INPUT_START, keys.c[0].start);

    /* Handle player 1 input */
    set_input_button(4, INPUT_RIGHT, keys.c[0].right);
    set_input_button(4, INPUT_LEFT, keys.c[0].left);
    set_input_button(4, INPUT_BACK, keys.c[0].down);
    set_input_button(4, INPUT_FORWARD, keys.c[0].up);
    set_input_button(4, INPUT_ROTATE_CCW, keys.c[0].C_left);
    set_input_button(4, INPUT_ROTATE_CW, keys.c[0].C_right);
    set_input_button(4, INPUT_PULL, keys.c[0].C_up);
    set_input_button(4, INPUT_PUSH, keys.c[0].C_down);

    pressf_run(&f8_system);

    draw_frame_ultra(((vram_t*)f8_system.f8devices[3].device)->data, pf_texture_a, pf_texture_b);

    surface_t frame_a = surface_make_linear(pf_texture_a, FMT_CI4, 64, 64);
    surface_t frame_b = surface_make_linear(pf_texture_b, FMT_CI4, 64, 64);
    surface_t *disp = display_get();

    rdpq_attach_clear(disp, NULL);
    rdpq_set_mode_standard();
    rdpq_mode_tlut(TLUT_RGBA16);
    rdpq_tex_blit(&frame_a, -6, 16, &(rdpq_blitparms_t){ .scale_x = 3.0f, .scale_y = 3.0f});
    rdpq_tex_blit(&frame_b, 64 * 3 - 6, 16, &(rdpq_blitparms_t){ .scale_x = 3.0f, .scale_y = 3.0f});
    rdpq_detach_show();

    audio_push(((f8_beeper_t*)f8_system.f8devices[7].device)->samples, PF_SOUND_SAMPLES, false);
  }
}
